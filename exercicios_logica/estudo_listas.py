# Criando uma lista ordenada e mutável
equipe = ["Cabo Felipe", "Sargento Silva", "Tenente Souza"]

print(f"Equipe original: {equipe}")

# Provando que é mutável: vamos trocar o Sargento
equipe[1] = "Sargento Oliveira"

# Provando que podemos adicionar itens
equipe.append("Soldado Santos")

print(f"Equipe atualizada: {equipe}")
