n1 = int(input("Digite o primeiro número: "))
n2 = int(input("Digite o segundo número: "))

print(f"{n1} + {n2} = {n1 + n2}")
print(f"{n1} - {n2} = {n1 - n2}")
print(f"{n1} * {n2} = {n1 * n2}")
print(f"{n1} / {n2} = {n1 / n2:.2f}") # :.2f formata para 2 casas decimais
print(f"{n1} // {n2} = {n1 // n2}")
print(f"{n1} % {n2} = {n1 % n2}")
print(f"{n1} ** {n2} = {n1 ** n2}")
